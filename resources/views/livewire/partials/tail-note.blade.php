<p class="mt-2 text-xs text-gray-500">
    <strong>Why the line can climb sharply at the far right:</strong> towards the end only a small number of the
    simulated futures still have someone alive (most couples have died by then). With so few left, the median rests
    on a handful of outcomes, so it jumps around — and it can drift up, because a long-lived survivor's guaranteed
    income (State Pension and any defined-benefit pension) often covers their reduced spending, leaving the
    remaining investment pot to keep growing. So the far tail is indicative, not a precise figure.
</p>
